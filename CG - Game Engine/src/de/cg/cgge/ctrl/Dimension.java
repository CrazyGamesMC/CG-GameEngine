package de.cg.cgge.ctrl;

public class Dimension {

    public int x = 0;
    public int y = 0;
    public int width = 0;
    public int height = 0;
    public int centerX = 0; 
    public int centerY = 0; 

    public Dimension(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }


}
